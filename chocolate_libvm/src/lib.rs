use wasm_bindgen::prelude::*;

pub mod instruction;
pub mod program;
pub mod virt;

pub fn add(left: u64, right: u64) -> u64 {
    left + right
}

#[wasm_bindgen]
extern "C" {
    pub fn alert(s: &str);
}

#[wasm_bindgen]
pub fn greet(name: &str) {
    alert(&format!("Hello, {}!", name));
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn it_works() {
        let result = add(2, 2);
        assert_eq!(result, 4);
    }
}
